import random

random_int = random.randint(1, 10)
print(f"this is a random number between 1 and 10: {random_int}")

random_number_0_1 = random.random()
print(f"this is a random number between 0 and 1: {random_number_0_1}")

random_float = random.uniform(1, 10)
print(f"this is a random float between 1 and 10: {random_float}")